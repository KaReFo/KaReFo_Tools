package com.karefo.word.suggestion.model;

public class Department {
    private int dept_id;
    private String dept_tamil;
    private String dept_english;

    public int getDept_id() {
        return dept_id;
    }
    public void setDept_id(int dept_id) {
        this.dept_id = dept_id;
    }

    public String getDept_tamil() {
        return dept_tamil;
    }
    public void setDept_tamil(String dept_tamil) {
        this.dept_tamil = dept_tamil;
    }

    public String getDept_english() {
        return dept_english;
    }
    public void setDept_english(String dept_english) {
        this.dept_english = dept_english;
    }
}
